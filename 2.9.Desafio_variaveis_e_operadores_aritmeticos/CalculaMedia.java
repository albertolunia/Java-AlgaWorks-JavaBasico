public class CalculaMedia {
    public static void main(String[] args) {
        double valor1, valor2, valor3, CalcularMedia;
        
        valor1 = 20;
        valor2 = 30;
        valor3 = 100;

        CalcularMedia = (valor1 + valor2 + valor3) / 3;

        System.out.println("A média dos valores é: " + CalcularMedia);
    }
}